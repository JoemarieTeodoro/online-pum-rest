package com.ph.ibm.model;

public class TotalLeaves {

	private int totalVL;
	private int totalSL; 
	private int totalOL; 
	private int totalEL; 
	private int totalTR; 
	private int totalHO; 
	private int totalCDO;
	
	public TotalLeaves() {
		this.totalVL = 0;
		this.totalSL = 0; 
		this.totalOL = 0; 
		this.totalEL = 0; 
		this.totalTR = 0; 
		this.totalHO = 0; 
		this.totalCDO = 0;
	}
	
	public int getTotalVL() {
		return totalVL;
	}
	public void setTotalVL(int totalVL) {
		this.totalVL = totalVL;
	}
	public int getTotalSL() {
		return totalSL;
	}
	public void setTotalSL(int totalSL) {
		this.totalSL = totalSL;
	}
	public int getTotalOL() {
		return totalOL;
	}
	public void setTotalOL(int totalOL) {
		this.totalOL = totalOL;
	}
	public int getTotalEL() {
		return totalEL;
	}
	public void setTotalEL(int totalEL) {
		this.totalEL = totalEL;
	}
	public int getTotalTR() {
		return totalTR;
	}
	public void setTotalTR(int totalTR) {
		this.totalTR = totalTR;
	}
	public int getTotalHO() {
		return totalHO;
	}
	public void setTotalHO(int totalHO) {
		this.totalHO = totalHO;
	}
	public int getTotalCDO() {
		return totalCDO;
	}
	public void setTotalCDO(int totalCDO) {
		this.totalCDO = totalCDO;
	}
	
}
